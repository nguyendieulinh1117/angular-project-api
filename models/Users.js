const mongoose = require('mongoose');

const UserSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    },
    typeUser: {
        type: Number,
        required: true,
        default: 0
    }
})

module.exports = mongoose.model('User', UserSchema);